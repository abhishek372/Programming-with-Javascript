// Javascript is a dynamic as well as weak typing programming language.

// Dynamic typing
let a = 50;         // a is of number data type

console.log(a);

a = "Hello";         // a is of string data type
console.log(a);

// Weak typing
let b = 5;          // b is of number data type
b += '5';           // b is now of string data type

console.log(b)
