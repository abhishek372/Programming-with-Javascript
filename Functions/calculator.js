// Arithmetic Calculator using function

function add(a,b){
    return a+b;
}
function subtract(a,b){
    return a-b;
}
function product(a,b){
    return a*b;
}
function divide(a,b){
    return a/b
}
function remainder(a,b){
    return a%b;
}

console.log(add(5,4));
console.log(subtract(5,4));
console.log(product(5,4));
console.log(divide(5,4));
console.log(remainder(5,4));

