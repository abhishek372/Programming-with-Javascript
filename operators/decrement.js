// Decrement (--) operator decrements (subtracts one from) its operand and returns the value before or after the decrement, depending on where the operator is placed.
// Post Decrement (a--)
// Pre Decrement (--a)

let x = 3;
const y = x--;
console.log(`x = ${x} \ny = ${y}`);

let a = 5;
const b = --a;
console.log(`a = ${a} and b = ${b}`)