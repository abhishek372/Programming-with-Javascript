// let keyword

// We cannot access a let variable before we declare it
// console.log('user');    // Error
let user;

// We can declare a unassigned variable with let
// user = 'Anna';
// console.log('user');

// We can't redeclare a declared variable
// let user = 'Mary';          //  Syntax Error
console.log(user);

// But we can reasssign a declared variable
user = 'Harry';
console.log(user);