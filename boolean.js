// Boolean
// Used to check the whether a statement is true or false
// Result can be either true or false

/* Additional Operators
    =   assignment operator
    ==  equality operator (checks only data value)
    === strictly equality operator (checks both data and value)
    !=  inequality operator (checks whether a value is not equal to other)
    !== strictly inequality operator (checks both value and type)
*/

// Returns boolean value
console.log(23 == "23")
console.log(23 === "23")
console.log(45!="43")
console.log(45!==45)


// Concatenate strings
var now = "Now in "
var three = 3
var d = "D!"
console.log(now+three+d)

// Concate strings and numbers
console.log(365 + " days")
console.log("He scored " + 100 + " centuries")

// += operator to increment values

var counter = 0
counter += 5
counter += 3
console.log("Counter =", counter)






